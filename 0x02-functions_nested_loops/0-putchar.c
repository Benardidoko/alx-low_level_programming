/*
 * File: _putchar.c
 *
 * Description: This program prints the string "_putchar" followed by a new line.
 *
 * Return: 0 on success.
 */

#include <stdio.h>

int main(void) {
    /* Print the string "_putchar" followed by a new line. */
    putchar('_');
    putchar('p');
    putchar('u');
    putchar('t');
    putchar('c');
    putchar('h');
    putchar('a');
    putchar('r');
    putchar('\n');

    return (0);
}

